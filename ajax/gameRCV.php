        <li>Trong mục <b>Don't start download automatically from the following site:</b>, thêm vào danh sách đoạn sau (có dấu cách ở đầu): <code style="text-decoration:underline">&nbsp;*.rcvbk.com</code></li>
        <li>Bấm <b>OK</b>.</li>
