    <h2>Hướng Dẫn</h2>
    <h3>Trước Khi Bắt Đầu</h3>
	<p>Các bạn nhập đầy đủ thông tin để có cơ hội nhận quà từ ban tổ chức:</p>
    <ul>
    	<li>Họ và tên (viết không dấu)</li>
		<li>Mã số sinh viên</li>
	</ul>
    
    <h3>Cách Chơi</h3>
    <ul>
		<li>Bấm <b>Spacebar</b> hoặc nhấp <b>Chuột Trái</b> để Chuông Vàng nhảy qua các khe cống và không được chạm cống hay chạm đất.</li>
		<li>Bấm <b>P</b> hay nhấp chuột góc trên bên phải để tạm dừng game. Bấm <b>Spacebar</b> để tiếp tục.</li>
		<li>Sau khi kết thúc game nhấp chuột hoặc bấm phím tắt <b>L</b> để chơi lại hay <b>C</b> để lấy mã dự thưởng.</li>
	</ul>

	<h3>Lấy Mã Dự Thưởng</h3>
	<ul>
    	<li>Sau khi bạn chọn lấy mã dự thưởng, sẽ có một thông báo <b>“Bạn có chắc chắn thoát game và lấy code?”</b> xuất hiện, chọn <b>OK</b>.</li>
		<li>Một thông báo mới hiện ra để bạn nhập số điện thoại, số điện thoại giúp ban tổ chức dễ liên lạc với bạn khi cần sau khi nhập. Sau đó, các bạn chọn <b>OK</b>.</li>
		<li>Cuối cùng, một thông báo hiện lên kèm một đoạn mã dự thưởng. Các bạn <b style="text-decoration: underline">bấm tổ hợp phím Ctrl+C</b> để sao chép nội dung mã dự thưởng. Sau đó, các bạn có thể gửi tin nhắn về cho ban tổ chức tại <a href="https://www.facebook.com/rungchuongvangbkhcm">Facebook</a> hoặc <a href="https://plus.google.com/u/3/111252708622018334512/posts">Google Plus</a> hoặc gửi về địa chỉ <b>rungchuongvangbkhcm@gmail.com</b>.</li>
    </ul>
    
    <h3>Lưu Ý</h3>
    <p>Có một số bạn sử dụng trình duyệt <b>Google Chrome</b> hoặc <b>Cốc Cốc</b> trong khi máy có sẵn <b>Internet Download Manager</b>. Việc này sẽ gây khó khăn trong việc tải âm thanh cho game. Các bạn nên làm theo hướng dẫn sau:</p>
    <ol>
