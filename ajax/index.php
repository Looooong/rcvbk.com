	<h1>Lời Giới Thiệu</h1>
	<p>Là hoạt động thường niên của khoa Khoa học và Kĩ thuật Máy tính thuộc trường đại học Bách Khoa thành phố Hồ Chí Minh, cuộc thi Rung Chuông Vàng chính thức bắt đầu từ năm 2012, qua 2 năm tổ chức đã thu hút sự tham gia của hàng trăm bạn sinh viên. Đây là một sân chơi hấp dẫn mang đến cho các bạn sinh viên nhiều kiến thức cũng như những thông tin vô cùng bổ ích và lý thú, cách chơi đơn giản, giải thưởng hấp dẫn, được sự hưởng ứng nhiệt tình của đông đảo SV qua các năm… tiếp nối những thành công đó, tháng 5 này chương trình sẽ chính thức khởi động, không chỉ giới hạn trong khuôn khổ trường ĐH BK, năm 2014, chương trình chính thức mở rộng phạm vi cho tất cả các bạn sinh viên đến từ nhiều trường ĐH, CĐ khác nhau trên địa bàn thành phố Hồ Chí Minh. Với sự nhiệt tình dạn dày kinh ngiệm, ban tổ chức kì vọng chương trình sẽ là cầu nối, kết nối những trái tim yêu thích học hỏi và tìm tòi tri thức, đẩy mạnh giao lưu, học hỏi giữa các Sinh viên trong Khoa KH và KT Máy tính và sinh viên các khoa khác, sinh viên Đại học Bách Khoa TP.HCM và sinh viên các trường Đại học trong khu vực TP.HCM.</p>
